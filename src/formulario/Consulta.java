    private void guardar() {
        Mconsulta m = new Mconsulta();
        Cconsulta c = new Cconsulta();
//        Mmedico medico = new Mmedico();
//        Mpaciente p = new Mpaciente();
        String sintoma = txtsintoma.getText();
        String data = txtdata.getText();
        if (txtsintoma.getText().equals("") || txtdata.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "PRENCHA OS ESPAÃ‡OS VAZIOS");

        } else {

//          m.setMedico(idme);
            m.setData(data);
            m.setSintoma(sintoma);
//            m.setMedico(medico);
            m.setPaciente(nomp);
            c.salvar(m);
        }
    }

    public void pesquisar(String p) {
        Cconsulta c = new Cconsulta();
        DefaultTableModel dtm = (DefaultTableModel) tbconsulta.getModel();
        Object[] titulo = {"CODIGO", "NOME DO PACIENTE", "SINTOMA", "DATA"};
        dtm.setColumnIdentifiers(titulo);
        dtm.setNumRows(0);
        for (Mconsulta m : c.pesquisar(p)) {
            Object[] dado = {m.getCodigo(), m.getPaciente(),
                m.getSintoma(), m.getData(), m.getMedico()};
            dtm.addRow(dado);
        }

    }

